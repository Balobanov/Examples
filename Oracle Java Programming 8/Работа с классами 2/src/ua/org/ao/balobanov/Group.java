package ua.org.ao.balobanov;

/**
 * Класс группы. Каждый студент числится в группе
 * @author DENNNN
 */
public class Group {
    private String course; // курс
    private String faculty; // факультет

    public Group(String course, String faculty) {
        this.course = course;
        this.faculty = faculty;
    }

    public String getCourse() {
        return course;
    }

    public void setCourse(String course) {
        this.course = course;
    }

    public String getFaculty() {
        return faculty;
    }

    public void setFaculty(String faculty) {
        this.faculty = faculty;
    }
    
    @Override
    public String toString()
    {
        return "Course: " + getCourse() + ".  Faculty: " + getFaculty();
    }
}
